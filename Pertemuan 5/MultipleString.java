public class MultipleString{

    String fname = "John";
    String lname = "Doe";
    int age = 24;

    public static void main(String[] args) {

        MultipleString OBJ1 = new MultipleString();
        System.out.println("Name : " + OBJ1.fname + "  " + OBJ1.lname);
        System.out.println("Age : " + OBJ1.age);
    }
}