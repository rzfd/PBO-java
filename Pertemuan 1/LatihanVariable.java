public class LatihanVariable{
    public static void main(String[] args) {
        int x = 100;
        int y = 200;
        int tambah = x + y;
        System.out.println("Nilai x adalah " + x +  " dan nilai y adalah " + y);
        System.out.println("Nilai x ditambah y adalah " +  tambah); 
        /* 
        total = 100 + 200 = 300
        */
        System.out.println("Total = " + x + " + " + y +  "=" + tambah); 
        
    }
}

